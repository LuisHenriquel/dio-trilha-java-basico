public class palavrasReservadas {

   //                                                 Palavras reservadas

   /*Palavras reservadas são indentificadores de uma linguagem que já possuem uma finalidadee específica,portanto não podem
     ser ultilizados para nomear variáveis, classes, métodos ou atributos.

     Alinguagem Java possui 52 palavras reservadas. todas essas palavras são classificadas em grupos e escritas com letras
     minúscula, sendo indentificado com uma cor especial pela maioria das IDE's. Abaixo temos a lista
     classificada de palavras agrupadas por sua finalidades.

     Controle de pacotes
     import: importa pacotes ou classes para detro do código

     package: especifica a que pacote todas as classes de um arquivo pertencem
-----
     Modificadores de acesso
     public: acesso de qualquer classe

     private: acesso apenas dentro da classe

     protected: acesso por classes no mesmo pacote e subclasses
---------
     Primitivos
     boolean: um valor indicando verdadeiro o falso

     byte: um inteiro de 8 bits (signed)

     char: um character unicode (16-bit-unsigned)

     double: um número de ponto flutuante de 64 bits (signed)

     float: um número de ponto flutuante de 32 bits (signed)

     int: um inteiro de 32 bits (signed)

     long um inteiro de 64 bits (signed)

     short: um inteiro de 32 bits (signed)

     void: indica que o método não tem retorno de valor
--------------
     Modificadores de classes, variáveis ou métodos

     abstract: classes que não pode ser instanciada ou método que prescisa ser implementado por uma subclasse não abstrata

     class:especifica uma classe

     extends: indica a superclasse que a subclasse está estendendo

     final: impossibilita que uma classe seja estendida, que um método seja
     sobrescrito ou que uma variável seja reinicializada

     implements:indica as interfaces que uma classe irá implementar

     interface:especifica uma interface

     native: indica que um método está escrito em uma linguagem dependente de plataforma, como o c

     new: instancia um novo objeto, chamado seu construtor

     static: faz um método ou variável pertencer á classe ao invés de ás instâncias

     strictfp: usado em frente a um método ou classe para indicar que os números de ponto flutuante seguirão as regras de ponto
     flutuante em todas as expressões

     synchonized: indica que um método só pode ser acessado por uma threade de cada vez

     transient: impede a serialização de campos

     volatile: indica que uma variável pode ser alterada durante o uso de threads
--------------
     Controle de fluxo dentro de um bloco de código
     break: sai do bloco de codigo em que ele está

     case:executa um bloco de código dependendo do teste do switch

     continue: pula a execução do código que viria após essa linha e vai para proxima passagem do loop
------------
     Variáveis de referência

     super: refere-se a superclasse imediata

     this:refere- se a instância do objeto
-----------------------------------
     Tratamento de erros
     assert: testa uma expressão condicional para verificar uma suposição do programador

     catch: declara o bloco de código usado para tratar uma exceção

     finally:bloco de código, aposo um try-catch, que é executado independentemente do fluxo de programa
     seguido ao lidar com uma exceção

     throw:usado para passar uma exceção para o método que o chamou

     throws:indica que um método pode passar uma exceção para o método que o chamou

     try: bloco de código que tentará ser executado, mas que pode causar uma exceção
--------

     Palavras "opostas"
     Assim como nas classificações gramaticais da língua portuguesa, existem algumas palavras que são completamente
     oposta (antônimas) na linguagem Java conforme tabela abaixo:

     package e import explicação:o package ela define o pacote da classe atual ja o import
     ele ja nao esta na mesma classe e tem que irmporta ele

     extends implements explicação: o extends ele estende uma classe ou uma classe pode implementar uma interface
     que é o implements

     final abstract explicação: o final é a ultima logica da minha aplicação ou do meu método ou o ultimo valor
     da minha variável ja a abstract é usado para o métodos que ele prescisa ser um pouco mais especifico atraves da herança

     throws throw explicação: throws ele dispara uma exceção ou ele determina um método que esse método pode gerar uma exceção
     o throw ele é a geração de uma exceção ele dispara uma nova exceção
  */
}

